#include <iostream>
#include <vector>

using namespace std;

/// <summary>
/// Завдання 2. Напишіть програму, яка створює двовимірний масив і заповнює його за таким принципом : 
/// користувач вводить число(наприклад, 3) перший елемент масиву приймає значення цього числа, 
/// наступний елемент масиву приймає значення цього числа + 1 (тобто 4 для нашогоприкладу), 
/// третій елемент масиву — попередній елемент + 1 (тобто 5 для нашого прикладу). Створений масив вивести на екран
/// </summary>
int main() {
    // Введення розміру масиву
    int rows, cols;
    cout << "Enter the number of rows: ";
    cin >> rows;
    cout << "Enter the number of columns: ";
    cin >> cols;

    // Введення початкового числа
    int startNumber;
    cout << "Enter the starting number: ";
    cin >> startNumber;

    // Створення двовимірного масиву за допомогою вектора
    vector<vector<int>> array(rows, vector<int>(cols));

    // Заповнення масиву за умовою
    array[0][0] = startNumber;  // Перший елемент масиву дорівнює введеному числу
    for (int i = 0; i < rows; ++i) {
        for (int j = 0; j < cols; ++j) {
            if (i == 0 && j == 0) continue;  // Пропускаємо перший елемент

            if (j == 0) {
                // Якщо це перший стовпець (але не перший рядок), додаємо 1 до останнього елементу попереднього рядка
                array[i][j] = array[i - 1][cols - 1] + 1;
            }
            else {
                // Інакше додаємо 1 до попереднього елементу в тому ж рядку
                array[i][j] = array[i][j - 1] + 1;
            }
        }
    }

    // Виведення масиву на екран
    for (int i = 0; i < rows; ++i) {
        for (int j = 0; j < cols; ++j) {
            cout << array[i][j] << "\t";  // Виведення елементу з табуляцією
        }
        cout << endl;
    }

    return 0;
}