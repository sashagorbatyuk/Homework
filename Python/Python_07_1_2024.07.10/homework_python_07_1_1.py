# Напишіть програму, яка запитує у користувача ім'я та вік. Після отримання даних програма повинна виводити привітання у форматі: "Привіт, {ім'я}!
# Твій вік — {вік}". Обробіть виняток, що виникає при введенні некоректного віку (вік < 0 або вік > 130), і виведіть повідомлення про помилку.

def main():
    try:
        # Запитуємо ім'я користувача
        name = input("Введіть ваше ім'я: ")

        # Запитуємо вік користувача
        age = int(input("Введіть ваш вік: "))

        # Перевіряємо коректність віку
        if age < 0 or age > 130:
            raise ValueError("Некоректний вік. Вік повинен бути в межах від 0 до 130 років.")

        # Виводимо привітання
        print(f"Привіт, {name}! Твій вік — {age}.")

    except ValueError as e:
        # Обробляємо помилку некоректного введення віку
        print(f"Помилка: {e}")

main()
